#include "iostream"
using namespace std;

int main() {
    // 整型
    int a = 0;
    cout << "请输入值：" << endl;
    cin >> a;
    cout << "a = " << a << endl;

    // 浮点型
    float f = 3.14f;
    cout << "请输入值：" << endl;
    cin >> f;
    cout << "f = " << f << endl;
}